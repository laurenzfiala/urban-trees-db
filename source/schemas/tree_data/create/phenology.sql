/**
 * Phenology data for a tree.
 * tree_data.phenology_observation stores all applicable observations.
 */

create table tree_data.phenology (
    ID					integer generated by default as identity primary key,
    observer			varchar(200) not null, 			-- name of observing person
    observe_dat			timestamp, 						-- observation time
    --
    cre_dat				timestamp,
    mod_dat				timestamp,
    cre_usr				varchar(20),
    mod_usr				varchar(20)
);